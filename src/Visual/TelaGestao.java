    /**
     * Realiza uma ExclusÃ£o LÃ³gica de um FuncionÃ¡rio do Banco de Dados,
     * mudando o seu Status de Ativo para ExcluÃ­do.
     * @param evt
     */
    private void BotaoExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotaoExcluirMouseClicked
        int linha = TabelaFuncionario.getSelectedRow();

        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Para excluir um FuncionÃ¡rio, vocÃª precisa selecionar uma linha!", "Erro ao Excluir um FuncionÃ¡rio!", JOptionPane.ERROR_MESSAGE);
        } else {
            int codigo = (int) TabelaFuncionario.getValueAt(linha, 0);
            if (funcionarioController.excluirFuncionario(codigo)) {
                carregarFuncionarios();
                limparFormulario();
                JOptionPane.showMessageDialog(this, "FuncionÃ¡rio excluÃ­do com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir FuncionÃ¡rio", "Erro!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BotaoExcluirMouseClicked

