public class ModelFuncionario {
    
    private int id;
    private String nome;
    private String cpf;
    private int telefone;
    private String endereco;
    private String login;
    private String senha;
    private int id_tipo_pessoa;
    private int id_curso;
    private int id_situacao;

    
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    
    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    
    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    
    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    
    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    
    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    
    /**
     * @return the telefone
     */
    public int getTelefone() {
        return telefone;
    }

    
    /**
     * @param telefone the telefone to set
     */
    public void setTelefone(int telefone) {
        this.telefone = telefone;
    }

    
    /**
     * @return the endereco
     */
    public String getEndereco() {
        return endereco;
    }

    
    /**
     * @param endereco the endereco to set
     */
    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    
    /**
     * @return the login
     */
    public String getLogin() {
        return login;
    }

    
    /**
     * @param login the login to set
     */
    public void setLogin(String login) {
        this.login = login;
    }

    
    /**
     * @return the senha
     */
    public String getSenha() {
        return senha;
    }

    
    /**
     * @param senha the senha to set
     */
    public void setSenha(String senha) {
        this.senha = senha;
    }

    
    /**
     * @return the id_tipo_pessoa
     */
    public int getId_tipo_pessoa() {
        return id_tipo_pessoa;
    }

    
    /**
     * @param id_tipo_pessoa the id_tipo_pessoa to set
     */
    public void setId_tipo_pessoa(int id_tipo_pessoa) {
        this.id_tipo_pessoa = id_tipo_pessoa;
    }

    
    /**
     * @return the id_curso
     */
    public int getId_curso() {
        return id_curso;
    }

    
    /**
     * @param id_curso the id_curso to set
     */
    public void setId_curso(int id_curso) {
        this.id_curso = id_curso;
    }

    
    /**
     * @return the id_situacao
     */
    public int getId_situacao() {
        return id_situacao;
    }

    
    /**
     * @param id_situacao the id_situacao to set
     */
    public void setId_situacao(int id_situacao) {
        this.id_situacao = id_situacao;
    }
}
